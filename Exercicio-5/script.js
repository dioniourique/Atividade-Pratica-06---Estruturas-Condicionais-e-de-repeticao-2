// Utilizando FOR, percorra os números de 1 a 10 e imprima no console
// os números pares.

// PseudoCodigo

// INÍCIO
//   PARA cada número i de 1 a 10 FAÇA
//     SE i é par ENTÃO
//       IMPRIMA i
//     FIM SE
//   FIM PARA
// FIM

for (let i = 1; i <= 10; i++) {
  if (i % 2 === 0) {
    console.log(i);
  }
}
